public class NumerosPares {
    public static void main(String[] args) {
        // Loop para imprimir os 100 primeiros números pares
        for (int i = 1; i <= 100; i++) {
            int par = i * 2; // Multiplica o contador por 2 para obter o número par
            System.out.println(par);
        }
    }
}
